int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	j;

	i = 0;
	while (i < length - 1)
	{
		j = 0;
		while (j < length - 1 - i)
		{
			if (f(tab[j], tab[j + 1]) > 0)
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
