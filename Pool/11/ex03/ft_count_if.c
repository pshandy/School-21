int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	count;
	int	j;

	j = 0;
	count = 0;
	while (j < length)
	{
		if (f(tab[j]) != 0)
		{
			count++;
		}
		j++;
	}
	return (count);
}
